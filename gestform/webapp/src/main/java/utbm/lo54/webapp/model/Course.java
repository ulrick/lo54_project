package utbm.lo54.webapp.model;

public class Course {

	private String code;
	private String title;
	
	public Course(){
		
	}
	
	public String getCode() {
		return code;
	}
	
	public void setCode(String code) {
		this.code = code;
	}
	
	public String getTitle() {
		return title;
	}
	
	public void setTitle(String title) {
		this.title = title;
	}
}
